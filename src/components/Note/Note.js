function Note(props) {
	const [state, dispatch] = Editor.useEditor(data)

	// Nav
	React.useLayoutEffect(() => {
		const nav = document.querySelector("nav")
		const { position } = nav.style
		nav.style.position = ""
		return () => {
			nav.style.position = position
		}
	}, [])

	// Footer
	React.useLayoutEffect(() => {
		const footer = document.querySelector("footer")
		const { display } = footer.style
		footer.style.display = "none"
		return () => {
			footer.style.display = display
		}
	}, [])

	// FIXME: Use context.
	return (
		<React.Fragment>
			<Editor.Editor state={state} dispatch={dispatch} mainInsetTop={80} mainInsetBottom={80} scrollPastEnd />
			<StatusBars state={state} dispatch={dispatch} />
		</React.Fragment>
	)
}

